package com.hongyu.entitycustom;

/**CouponSMSController中使用    使jsp能够展示内容*/
public class CouponForSMS {
	public String code;
	public String activateCode;
	public String isActivied;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getActivateCode() {
		return activateCode;
	}

	public void setActivateCode(String activateCode) {
		this.activateCode = activateCode;
	}

	public String getIsActivied() {
		return isActivied;
	}

	public void setIsActivied(String isActivied) {
		this.isActivied = isActivied;
	}
	
	
}